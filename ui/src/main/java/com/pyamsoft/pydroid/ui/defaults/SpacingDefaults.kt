package com.pyamsoft.pydroid.ui.defaults

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/** Default values for Spacing */
public object SpacingDefaults {

  /** Spacing unit for keylines */
  public val Keyline: Dp = 8.dp

  /** Spacing unit for padding */
  public val Padding: Dp = Keyline * 2

  /** Spacing unit for adjustments */
  public val Adjustment: Dp = Keyline / 2

  /** Spacing unit for hairline */
  public val Hairline: Dp = 1.dp
}
